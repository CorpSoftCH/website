export class ProduktClass {
    constructor(
        public id: string,
        public title: string,
        public subtitles: Array<string>,
        public products: Array<Produkt>,
    ){}

}

export class Produkt {
    constructor(
        public size: string,
        public mindLizenzen: number,
        public content: Array<Content>,
        public button: string,
    ){}

    public getLizImgPath():string {
        return "app/data/images/produkte/lizenzen/stamp_" + this.mindLizenzen + ".png";
    }

    public isFullPackage():boolean {
        if (this.size == "Full Package") {
            return true;
        } else {
            return false;
        }
        
    }

    public setFPkgClass():string {
        if( this.isFullPackage() ) {
            return "fullPkg";
        } else {
            return "";
        }
    }
}

export class Content {
    constructor(
        public title: string,
        public list: Array<String>,
        public imgPath: string,
        public lizenzen: Array<number> = []
    ){}
}

/**
 * Die Funktion in dieser Klasse übergibt die Produkte der Komponente.
 */
export class ProduktService {
  
  public getProdukte(): Array<ProduktClass> {
    var all = allProducts.map(pc => new ProduktClass(pc.id, pc.title, pc.subtitles, pc.products.map(p => new Produkt(p.size, p.mindLizenzen, p.content.map(c => new Content(c.title, c.list, c.imgPath, c.lizenzen)) ,p.button))));
    return all;  
  }
}